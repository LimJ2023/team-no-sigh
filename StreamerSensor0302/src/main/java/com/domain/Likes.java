package com.domain;

public class Likes {

	private String user_id;
	private String streamer_id;
	private int favorites;
	private int stream_categorys_id;
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getStreamer_id() {
		return streamer_id;
	}
	public void setStreamer_id(String streamer_id) {
		this.streamer_id = streamer_id;
	}
	public int getFavorites() {
		return favorites;
	}
	public void setFavorites(int favorites) {
		this.favorites = favorites;
	}
	public int getStream_categorys_id() {
		return stream_categorys_id;
	}
	public void setStream_categorys_id(int stream_categorys_id) {
		this.stream_categorys_id = stream_categorys_id;
	}
	
	
	
}
